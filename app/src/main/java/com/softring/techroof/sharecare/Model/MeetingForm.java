package com.softring.techroof.sharecare.Model;

public class MeetingForm {
    public String uid, time, date, description, image, name, p_age, p_careAddress, p_date, p_disease, p_mobile, p_name, p_time;

    public MeetingForm() {
    }

    public MeetingForm(String uid, String time, String date, String description, String image, String name, String p_age, String p_careAddress, String p_date, String p_disease, String p_mobile, String p_name, String p_time) {
        this.uid = uid;
        this.time = time;
        this.date = date;
        this.description = description;
        this.image = image;
        this.name = name;
        this.p_age = p_age;
        this.p_careAddress = p_careAddress;
        this.p_date = p_date;
        this.p_disease = p_disease;
        this.p_mobile = p_mobile;
        this.p_name = p_name;
        this.p_time = p_time;
    }

    public String getUid() {
        return uid;
    }

    public void setUid(String uid) {
        this.uid = uid;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getP_age() {
        return p_age;
    }

    public void setP_age(String p_age) {
        this.p_age = p_age;
    }

    public String getP_careAddress() {
        return p_careAddress;
    }

    public void setP_careAddress(String p_careAddress) {
        this.p_careAddress = p_careAddress;
    }

    public String getP_date() {
        return p_date;
    }

    public void setP_date(String p_date) {
        this.p_date = p_date;
    }

    public String getP_disease() {
        return p_disease;
    }

    public void setP_disease(String p_disease) {
        this.p_disease = p_disease;
    }

    public String getP_mobile() {
        return p_mobile;
    }

    public void setP_mobile(String p_mobile) {
        this.p_mobile = p_mobile;
    }

    public String getP_name() {
        return p_name;
    }

    public void setP_name(String p_name) {
        this.p_name = p_name;
    }

    public String getP_time() {
        return p_time;
    }

    public void setP_time(String p_time) {
        this.p_time = p_time;
    }

}
